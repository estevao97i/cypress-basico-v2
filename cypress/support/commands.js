Cypress.Commands.add('fillMandatoryFieldsAndSubmit', (user) => {
    cy
    .get('#firstName').type(user.firstName)
    .get('#lastName').type(user.lastName)
    .get('#email').type(user.email)
    .get('#open-text-area').type(user.textArea)
})
